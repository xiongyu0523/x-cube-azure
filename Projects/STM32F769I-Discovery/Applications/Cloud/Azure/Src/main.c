/**
  * @brief RTC init function
  */
static void RTC_Init(void)
{
  hrtc.Instance = RTC;
  hrtc.Init.HourFormat = RTC_HOURFORMAT_24;
  /* 32kHz LSI clock input */
  hrtc.Init.AsynchPrediv = 99;
  hrtc.Init.SynchPrediv = 319;
  hrtc.Init.OutPut = RTC_OUTPUT_DISABLE;
#ifdef RTC_OUTPUT_REMAP_NONE
  hrtc.Init.OutPutRemap = RTC_OUTPUT_REMAP_NONE;
#endif
  hrtc.Init.OutPutPolarity = RTC_OUTPUT_POLARITY_HIGH;
  hrtc.Init.OutPutType = RTC_OUTPUT_TYPE_OPENDRAIN;
  if (HAL_RTC_Init(&hrtc) != HAL_OK)
  {
    Error_Handler();
  }
}

/**
  * @brief  Start Thread
  * @param  argument not used
  * @retval None
  */
static void MainThread(void const * argument)
{
  osThreadId CloudThreadId = NULL;
  UNUSED(argument);
  msg_info("\r\n Starting Main Thread...\n");

  platform_init();

  /* Start application task */
  /* Beware that the OS stack frames are allocated from the OS heap. May incur RAM overwrite in case of overflow. */
  osThreadDef(CLOUDNAME, &cloud_run, osPriorityNormal, 0, configMINIMAL_STACK_SIZE + 0x1000);
  CloudThreadId = osThreadCreate (osThread(CLOUDNAME), NULL);

  if (CloudThreadId == NULL)
  {
     Error_Handler();
  }

  for( ;; )
  {
    /* Delete the start Thread */
    osThreadTerminate(NULL);
  }
}

/**
  * @brief  EXTI line detection callback.
  * @param  GPIO_Pin: Specifies the port pin connected to corresponding EXTI line.
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  switch (GPIO_Pin)
  {
    case (GPIO_PIN_0):
    {
      Button_ISR();
      break;
    }
    default:
    {
      break;
    }
  }
}

/**
  * @brief  This function is executed in case of error occurrence.
  * @param  None
  * @retval None
  */
void Error_Handler(void)
{
  while(1)
  {
    BSP_LED_Toggle(LED_GREEN);
    HAL_Delay(200);
  }
}

