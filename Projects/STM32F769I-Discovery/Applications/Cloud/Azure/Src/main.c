/**
  * @brief  Main program
  * @param  None
  * @retval Status
  */
int main(void)
{
  unsigned int random_number = 0;
  osThreadId MainThreadId = NULL;

  MPU_Config();
  /* Enable the CPU Cache */
  CPU_CACHE_Enable();

  HAL_Init();
  
  /* Configure the system clock */
  SystemClock_Config();

  Periph_Config();
  
  BSP_LED_Init(LED_GREEN);
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);
  
  /* RNG init function */
  hrng.Instance = RNG;
  if (HAL_RNG_Init(&hrng) != HAL_OK)
  {
    Error_Handler();
  }
  else if (HAL_RNG_GenerateRandomNumber(&hrng, (uint32_t *) &random_number) == HAL_OK)
  {
    /* Initialize the seed of the stdlib rand() SW implementation. */
    srand(random_number);
  }
  
  /* RTC init */
  RTC_Init();
  /* UART console init */
  Console_UART_Init();

#if (defined(__GNUC__) && !defined(__CC_ARM))
  /* Do not buffer stdout, so that single chars are output without any delay to the console. */
  setvbuf(stdout, NULL, _IONBF, 0);
#endif

   /* Init thread */
  osThreadDef(Start, MainThread, osPriorityNormal, 0, configMINIMAL_STACK_SIZE + 1000);
  MainThreadId = osThreadCreate (osThread(Start), NULL);

  if (MainThreadId == NULL)
  {
     Error_Handler();
  }
  
  /* Start scheduler */
  osKernelStart();
  
  /* We should never get here as control is now taken by the scheduler */
  Error_Handler();
}


